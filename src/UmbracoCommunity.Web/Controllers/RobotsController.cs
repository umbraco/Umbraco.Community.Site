using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Hosting;
using System.Text;

namespace UmbracoCommunity.Web.Controllers;

public class RobotsController : Controller
{
    private readonly IWebHostEnvironment _webHostEnvironment;

    public RobotsController(IWebHostEnvironment webHostEnvironment) => _webHostEnvironment = webHostEnvironment;

    public IActionResult Index()
    {
        var builder = new StringBuilder();

        builder.AppendLine("User-agent: *");

        if (_webHostEnvironment.IsDevelopment() || _webHostEnvironment.IsStaging())
        {
            builder.AppendLine("Disallow: /");
        }
        else
        {
            builder.AppendLine("Sitemap: /sitemap/");
            builder.AppendLine("Disallow: /App_Plugins/");
            builder.AppendLine("Disallow: /runtimes/");
            builder.AppendLine("Disallow: /wwwroot/");
            builder.AppendLine("Disallow: /umbraco/");
            builder.AppendLine("Disallow: /views/");
        }

        builder.AppendLine();
        builder.Append(GetUmbracoTextLogo());
        
        return Content(builder.ToString(), "text/plain");
    }

